CREATE TABLE DB_LOG   (  
   LOG_NUM VARCHAR2(100),
   THREAD_NAME VARCHAR2(100),
   LOG_TIMESTAMP VARCHAR2(14),
   MESSAGE VARCHAR2(4000),
   LOGGER_NAME VARCHAR2(100),
   LOG_LEVEL VARCHAR2(10),
   NDC VARCHAR2(100),
   START_TIME VARCHAR2(14),
   CALLER_FILENAME VARCHAR2(100),
   CALLER_CLASS VARCHAR2(100),
   CALLER_METHOD VARCHAR2(100),
   CALLER_LINE VARCHAR2(10)
);

CREATE SEQUENCE logSequence START WITH 1 INCREMENT BY 1;
