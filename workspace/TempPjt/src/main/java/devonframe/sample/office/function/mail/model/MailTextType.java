package devonframe.sample.office.function.mail.model;


public class MailTextType {

    private String textTypeCode;

    private String textTypeValue;

    /**
     * @return the textType
     */
    public String getTextTypeCode() {
        return textTypeCode;
    }

    /**
     * @param textType the textType to set
     */
    public void setTextTypeCode(String textTypeCode) {
        this.textTypeCode = textTypeCode;
    }

    /**
     * @return the textValue
     */
    public String getTextTypeValue() {
        return textTypeValue;
    }

    /**
     * @param textValue the textValue to set
     */
    public void setTextTypeValue(String textTypeValue) {
        this.textTypeValue = textTypeValue;
    }

}
